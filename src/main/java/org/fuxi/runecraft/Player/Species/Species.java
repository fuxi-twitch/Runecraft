package org.fuxi.runecraft.Player.Species;

public abstract class Species {
    private byte strMultiplier;
    private byte dexMultiplier;
    private byte conMultiplier;
    private byte intMultiplier;
    private byte wisMultiplier;
    private byte chaMultiplier;
    private byte speed;
    private float size;

    public byte getStrMultiplier() {
        return dexMultiplier;
    }

    public byte getDexMultiplier() {
        return dexMultiplier;
    }

    public byte getConMultiplier() {
        return dexMultiplier;
    }

    public byte getIntMultiplier() {
        return dexMultiplier;
    }

    public byte getWisMultiplier() {
        return dexMultiplier;
    }

    public byte getChaMultiplier() {
        return dexMultiplier;
    }

    public byte getSpeed() {
        return speed;
    }

    public double getSize() {
        return size;
    }
}
